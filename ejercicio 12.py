'''Ejercicio 12: Calculadora de Área de un Rectángulo
Crea un programa que calcule el área de un rectángulo. El usuario debe ingresar la
longitud y el ancho del rectángulo'''

longitud = float(input('indica la longitud del rectángulo en cm: '))

ancho = float(input('indique el ancho del rectángulo en cm: '))

area = longitud * ancho

print('el área del rectangulo es: ', area,'cm2')